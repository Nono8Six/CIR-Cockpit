const normalizeReferenceCode = (value: string | null | undefined): string | null => {
  const normalized = value?.trim().toUpperCase() ?? '';
  return normalized.length > 0 ? normalized : null;
};

export const formatOfficialRegion = (value: string | null | undefined): string | null => {
  const code = normalizeReferenceCode(value);
  if (!code) {
    return null;
  }

  return REGION_LABELS[code] ?? code;
};

export const normalizeOfficialNafCode = (value: string | null | undefined): string | null =>
  normalizeReferenceCode(value);

export const resolveOfficialNafCode = (
  ...values: Array<string | null | undefined>
): string | null => {
  for (const value of values) {
    const code = normalizeReferenceCode(value);
    if (code) {
      return code;
    }
  }

  return null;
};

export const getOfficialNafLabel = (value: string | null | undefined): string | null => {
  const code = normalizeReferenceCode(value);
  if (!code) {
    return null;
  }

  return NAF_2025_LABELS[code] ?? NAF_REV2_LABELS[code] ?? null;
};

export const formatOfficialNaf = (value: string | null | undefined): string | null => {
  const code = normalizeReferenceCode(value);
  if (!code) {
    return null;
  }

  const label = getOfficialNafLabel(code);
  return label ? `${code} - ${label}` : code;
};
